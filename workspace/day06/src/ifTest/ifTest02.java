package ifTest;
//10번 : if~else 문
public class ifTest02 {//클래스 시작
	public static void main(String[] args) {//main 시작
		System.out.println("출력 시작");
		
		if(false) {//if 문 영역
			System.out.println("조건식이 true입니다");
		}
		else {//else 문 영역
			System.out.println("조건식이 false 입니다");
		}
		System.out.println("출력 끝");
	}//main 종료
}//클래스 종료
